public class Musica {
    
    private String enderecoMusica;
    private String nomeMusica;
    private String tempoMusica;

    public String getEnderecoMusica() {
        return enderecoMusica;
    }

    public void setEnderecoMusica(String enderecoMusica) {
        this.enderecoMusica = enderecoMusica;
    }

    public String getNomeMusica() {
        return nomeMusica;
    }

    public void setNomeMusica(String nomeMusica) {
        this.nomeMusica = nomeMusica;
    }

    public String getTempoMusica() {
        return tempoMusica;
    }

    public void setTempoMusica(String tempoMusica) {
        this.tempoMusica = tempoMusica;
    }
    
    
    
}
